"""
NLM Admin API — FastAPI backend
Bookmarklet-based auth: recebe cookies do browser do operador via fetch(),
salva em /root/.notebooklm-mcp-cli/cookies.txt para o MCP server usar.
Zero VNC/Chromium inside Docker.
"""

import os
import subprocess
import time
from pathlib import Path

from fastapi import FastAPI, HTTPException
from fastapi.responses import FileResponse, HTMLResponse
from fastapi.staticfiles import StaticFiles

app = FastAPI(title="NLM Auth Manager", docs_url=None, redoc_url=None)

ADMIN_SECRET = os.getenv("ADMIN_SECRET", "c8club-nlm-admin")
COOKIES_DIR = Path("/root/.notebooklm-mcp-cli")
COOKIES_FILE = COOKIES_DIR / "cookies.txt"

# Auth state
_auth = {"status": "idle", "account": None}


def _verify(secret: str):
    if secret != ADMIN_SECRET:
        raise HTTPException(status_code=401, detail="Acesso não autorizado")


# ── Bookmarklet Auth Endpoint ──────────────────────────────────────────────────

@app.post("/api/auth-bookmarklet")
async def auth_bookmarklet(body: dict):
    """
    Recebe cookies de:
    - Chrome Extension: objetos completos via chrome.cookies.getAll() (inclui HttpOnly)
    - Bookmarklet fallback: string de document.cookie (sem HttpOnly)
    Salva em formato Netscape para o MCP server usar.
    """
    _verify(body.get("secret", ""))

    source = body.get("source", "bookmarklet")
    url = body.get("url", "")
    cookie_objects = body.get("cookies", [])  # Full objects from extension
    cookie_string = body.get("cookies_string", body.get("cookies") if isinstance(body.get("cookies"), str) else "")

    # Validate we're getting google cookies
    if isinstance(cookie_objects, str):
        # Legacy string format
        cookie_string = cookie_objects
        cookie_objects = []

    if not cookie_objects and not cookie_string:
        raise HTTPException(status_code=400, detail="Nenhum cookie recebido")

    COOKIES_DIR.mkdir(parents=True, exist_ok=True)

    try:
        lines = ["# Netscape HTTP Cookie File", "# Generated by NLM Admin Manager - C8Club"]
        expiry_far = int(time.time()) + (86400 * 365)  # 1 year

        if cookie_objects and isinstance(cookie_objects, list) and len(cookie_objects) > 0:
            # Chrome extension: full cookie objects (includes HttpOnly cookies!)
            for c in cookie_objects:
                if not isinstance(c, dict):
                    continue
                name = c.get("name", "")
                value = c.get("value", "")
                domain = c.get("domain", ".google.com")
                path = c.get("path", "/")
                secure = "TRUE" if c.get("secure", False) else "FALSE"
                expiry = int(c.get("expirationDate", expiry_far))
                include_sub = "TRUE" if domain.startswith(".") else "FALSE"
                if not domain.startswith("."):
                    domain = "." + domain
                lines.append(f"{domain}\t{include_sub}\t{path}\t{secure}\t{expiry}\t{name}\t{value}")
        elif cookie_string:
            # Bookmarklet fallback: "key=value; key2=value2" format
            for part in cookie_string.split(";"):
                part = part.strip()
                if "=" in part:
                    key, _, val = part.partition("=")
                    lines.append(f".google.com\tTRUE\t/\tFALSE\t{expiry_far}\t{key.strip()}\t{val.strip()}")
                    lines.append(f".notebooklm.google.com\tTRUE\t/\tFALSE\t{expiry_far}\t{key.strip()}\t{val.strip()}")

        COOKIES_FILE.write_text("\n".join(lines) + "\n")
        _auth["status"] = "authenticated"

        count = len(cookie_objects) if cookie_objects else len(cookie_string.split(";"))
        via = "extensão Chrome" if source == "chrome_extension" else "bookmarklet"
        return {"message": f"✅ {count} cookies autenticados via {via}! O sistema NLM está ativo."}

    except Exception as e:
        raise HTTPException(status_code=500, detail=f"Erro ao salvar cookies: {str(e)}")


# ── Status Endpoint ────────────────────────────────────────────────────────────

@app.get("/api/status")
async def get_status():
    # Check file-based auth state
    if COOKIES_FILE.exists() and COOKIES_FILE.stat().st_size > 100:
        _auth["status"] = "authenticated"
    else:
        # Also try nlm --check if available
        try:
            r = subprocess.run(
                ["nlm", "login", "--check"],
                capture_output=True, text=True, timeout=5,
            )
            if r.returncode == 0 and "authenticated" in (r.stdout + r.stderr).lower():
                _auth["status"] = "authenticated"
        except Exception:
            pass

    return {"status": _auth["status"], "account": _auth["account"]}


@app.get("/health")
async def health():
    return {"status": "ok", "service": "nlm-admin"}


# ── Static Files ───────────────────────────────────────────────────────────────
static_dir = Path(__file__).parent / "static"
static_dir.mkdir(exist_ok=True)
app.mount("/static", StaticFiles(directory=str(static_dir)), name="static")


@app.get("/", response_class=HTMLResponse)
async def root():
    index = static_dir / "index.html"
    if index.exists():
        return FileResponse(str(index))
    return HTMLResponse("<h1>NLM Admin</h1>")
